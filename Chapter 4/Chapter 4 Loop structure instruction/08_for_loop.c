// for loop syntax
// for (initialize; test; increment or decrement) {
//      code;
// }
// initialize - setting a loop counter to zero
// test - checking condition
// increment or decrement - updating the loop counter

#include<stdio.h>

int main() {
    int i;
    for (i=0;i<3;i++) {
        printf("%d\n",i);
    }
}